SpriteSet::SpriteSet() {
    namespace fs = std::filesystem;

    auto load = [](sf::Texture& tex, const char* path, sf::Image fallback) {
        if (!tex.loadFromFile(path)) {
            [[maybe_unused]] bool ok = tex.loadFromImage(fallback);
            try {
                fs::create_directories("sprites");
                [[maybe_unused]] bool saved = fallback.saveToFile(path);
            } catch (...) {}
        }
        tex.setSmooth(false);
    };

    load(heroTex,   "sprites/hero.png",   fromData(HP,  HC));
    load(goblinTex, "sprites/goblin.png", fromData(GP,  GC));
    load(trollTex,  "sprites/troll.png",  fromData(TRP, TRC));
    load(chestTex,  "sprites/chest.png",  fromData(ChP, ChC));
}
